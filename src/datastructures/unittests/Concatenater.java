package datastructures.unittests;

/**
 * This class is used for concatenation.
 */
public class Concatenater {

    /**
     * Concatenates two string word together into one.
     *
     * @param word1 The first String word.
     * @param word2 The second String word.
     * @return the concatenated word.
     */
    public static String concatenateStrings(String word1, String word2) {
        return word1 + word2;
    }

}
